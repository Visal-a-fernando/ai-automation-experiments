#!/usr/bin/env python3
"""
Generate a fillable case record PDF for the troubleshooting-wizard skill.

Usage (steps as repeatable --step flags):
    python generate_case_pdf.py \
        --client "Acme Corp" \
        --symptom "User can't send external email from Outlook" \
        --environment "Windows 11, Outlook 365 v2401, M365 E3" \
        --step "Verified send/receive folders for stuck items" \
        --step "Checked SMTP auth in Exchange admin centre" \
        --step "Ran Get-OrganizationConfig to confirm Modern Auth" \
        --root-cause "Modern Auth disabled at tenant level" \
        --resolution "1. Enabled Modern Auth via Set-OrganizationConfig\n2. Restarted Outlook" \
        --lesson "Modern Auth (OAuth 2.0) is required for any client that supports MFA..." \
        --out-dir ./case-log

Each --step renders as a block with a fillable Result textarea and an
Outcome field, so Visal can document what happened at each step in the PDF.

Requires: reportlab (pip install reportlab)
"""
import argparse
import re
from datetime import datetime
from pathlib import Path

from reportlab.lib.pagesizes import A4
from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
from reportlab.lib.units import mm
from reportlab.lib.colors import HexColor, black, grey
from reportlab.pdfgen import canvas
from reportlab.platypus import Paragraph
from reportlab.lib.enums import TA_LEFT

EMPTY = "(none)"

PAGE_BOTTOM_MARGIN_MM = 20
TOP_RESET_MM = 20


def next_case_number(out_dir: Path) -> str:
    """Auto-increment CASE-YYYY-NNNN based on files already in case-log."""
    year = datetime.now().year
    pattern = re.compile(rf"CASE-{year}-(\d{{4}})")
    highest = 0
    for f in out_dir.glob(f"CASE-{year}-*.pdf"):
        m = pattern.search(f.name)
        if m:
            highest = max(highest, int(m.group(1)))
    return f"CASE-{year}-{(highest + 1):04d}"


def slug(text: str, max_len: int = 40) -> str:
    s = re.sub(r"[^a-zA-Z0-9\s-]", "", text.lower())
    s = re.sub(r"\s+", "-", s).strip("-")
    return s[:max_len]


def clean(text: str) -> str:
    """Strip em/en dashes and double-hyphens. Visal's house rule."""
    if not text:
        return ""
    return (
        text.replace("—", ", ")  # em dash
        .replace("–", " to ")  # en dash
        .replace("--", ", ")
    )


def draw_wrapped(c, text, x, y, width, style):
    p = Paragraph(clean(text).replace("\n", "<br/>"), style)
    _, h = p.wrap(width, 1000)
    p.drawOn(c, x, y - h)
    return y - h - 4


def draw_section_header(c, title, x, y):
    c.setFillColor(HexColor("#1f3a5f"))
    c.setFont("Helvetica-Bold", 11)
    c.drawString(x, y, title.upper())
    c.setStrokeColor(HexColor("#1f3a5f"))
    c.setLineWidth(0.5)
    c.line(x, y - 2, x + 170 * mm, y - 2)
    c.setFillColor(black)
    return y - 8


def ensure_space(c, y, needed_mm, width, height, margin):
    """Start a new page if there isn't enough room left for `needed_mm` of content."""
    if y - needed_mm * mm < PAGE_BOTTOM_MARGIN_MM * mm:
        draw_footer(c, width)
        c.showPage()
        return height - TOP_RESET_MM * mm
    return y


def draw_footer(c, width):
    c.setFont("Helvetica-Oblique", 8)
    c.setFillColor(grey)
    c.drawCentredString(
        width / 2,
        12 * mm,
        "Generated by Visal's Work Agent. Fillable fields can be edited in any PDF reader.",
    )
    c.setFillColor(black)


def generate(args):
    out_dir = Path(args.out_dir).expanduser().resolve()
    out_dir.mkdir(parents=True, exist_ok=True)

    case_num = next_case_number(out_dir)
    date_str = datetime.now().strftime("%Y-%m-%d")
    filename = f"{case_num}-{slug(args.symptom)}.pdf"
    filepath = out_dir / filename

    c = canvas.Canvas(str(filepath), pagesize=A4)
    width, height = A4
    margin = 20 * mm
    text_width = width - 2 * margin

    styles = getSampleStyleSheet()
    body = ParagraphStyle(
        "body",
        parent=styles["BodyText"],
        fontName="Helvetica",
        fontSize=10,
        leading=13,
        alignment=TA_LEFT,
        textColor=black,
    )
    howto = ParagraphStyle(
        "howto",
        parent=styles["BodyText"],
        fontName="Helvetica-Oblique",
        fontSize=9,
        leading=12,
        alignment=TA_LEFT,
        textColor=HexColor("#3a3a3a"),
        leftIndent=14 * mm,
    )
    qa_q = ParagraphStyle(
        "qa_q",
        parent=styles["BodyText"],
        fontName="Helvetica-Bold",
        fontSize=10,
        leading=13,
        alignment=TA_LEFT,
        textColor=HexColor("#1f3a5f"),
    )
    qa_a = ParagraphStyle(
        "qa_a",
        parent=styles["BodyText"],
        fontName="Helvetica",
        fontSize=10,
        leading=13,
        alignment=TA_LEFT,
        textColor=black,
        leftIndent=6 * mm,
    )

    # Header bar
    c.setFillColor(HexColor("#1f3a5f"))
    c.rect(0, height - 25 * mm, width, 25 * mm, fill=1, stroke=0)
    c.setFillColor(HexColor("#ffffff"))
    c.setFont("Helvetica-Bold", 18)
    c.drawString(margin, height - 15 * mm, "IT Support Case Record")
    c.setFont("Helvetica", 10)
    c.drawString(
        margin,
        height - 21 * mm,
        f"{case_num}  |  {date_str}  |  Engineer: {args.engineer}",
    )
    c.drawRightString(width - margin, height - 21 * mm, f"Client: {clean(args.client)}")
    c.setFillColor(black)

    y = height - 35 * mm

    # Symptom
    y = draw_section_header(c, "Symptom", margin, y)
    y = draw_wrapped(c, args.symptom or EMPTY, margin, y, text_width, body)
    y -= 4

    # Environment
    y = ensure_space(c, y, 25, width, height, margin)
    y = draw_section_header(c, "Environment", margin, y)
    y = draw_wrapped(c, args.environment or EMPTY, margin, y, text_width, body)
    y -= 4

    # Conversation log (intake questions + Visal's answers)
    questions = args.question or []
    answers = args.answer or []
    answers = answers + [""] * max(0, len(questions) - len(answers))
    if questions:
        y = ensure_space(c, y, 20, width, height, margin)
        y = draw_section_header(c, "Conversation Log", margin, y)
        for q_text, a_text in zip(questions, answers):
            y = ensure_space(c, y, 20, width, height, margin)
            y = draw_wrapped(
                c, f"Q. {clean(q_text)}", margin, y, text_width, qa_q
            )
            y = draw_wrapped(
                c, f"A. {clean(a_text) or '(not answered)'}",
                margin, y, text_width - 6 * mm, qa_a,
            )
            y -= 2
        y -= 4

    # Diagnostic steps with per-step fillable Result + Outcome
    y = ensure_space(c, y, 20, width, height, margin)
    y = draw_section_header(c, "Diagnostic Steps", margin, y)

    steps = args.step or []
    hows = args.step_how or []
    # Pad hows to match steps so zip-like indexing is safe
    hows = hows + [""] * max(0, len(steps) - len(hows))

    if not steps:
        y = draw_wrapped(c, EMPTY, margin, y, text_width, body)
        y -= 4
    else:
        for idx, (step_text, how_text) in enumerate(zip(steps, hows), start=1):
            # Worst case block height: title + how-to + textarea + outcome
            y = ensure_space(c, y, 75, width, height, margin)

            # Step description
            c.setFont("Helvetica-Bold", 10)
            c.setFillColor(black)
            c.drawString(margin, y - 4, f"Step {idx}.")
            y = draw_wrapped(
                c, clean(step_text), margin + 14 * mm, y, text_width - 14 * mm, body
            )

            # How-to guidance (printed, not fillable)
            if how_text and how_text.strip():
                c.setFont("Helvetica-Oblique", 9)
                c.setFillColor(grey)
                c.drawString(margin + 14 * mm, y - 4, "How to do this:")
                c.setFillColor(black)
                y -= 8
                y = draw_wrapped(
                    c,
                    clean(how_text),
                    margin,
                    y,
                    text_width - 14 * mm,
                    howto,
                )
                y -= 2

            # Result label + textarea
            c.setFont("Helvetica", 9)
            c.setFillColor(grey)
            c.drawString(margin, y - 4, "Result / observation:")
            c.setFillColor(black)
            c.acroForm.textfield(
                name=f"step_{idx}_result",
                tooltip=f"What happened when you ran step {idx}",
                x=margin,
                y=y - 32,
                width=text_width,
                height=24,
                borderColor=grey,
                fillColor=HexColor("#f5f7fa"),
                textColor=black,
                forceBorder=True,
                fontSize=10,
                fieldFlags="multiline",
            )
            y -= 36

            # Outcome label + single-line field
            c.setFont("Helvetica", 9)
            c.setFillColor(grey)
            c.drawString(margin, y - 4, "Outcome (resolved / next step / escalate):")
            c.setFillColor(black)
            c.acroForm.textfield(
                name=f"step_{idx}_outcome",
                tooltip="Did this resolve it, move to next step, or escalate?",
                x=margin + 80 * mm,
                y=y - 9,
                width=text_width - 80 * mm,
                height=12,
                borderColor=grey,
                fillColor=HexColor("#f5f7fa"),
                textColor=black,
                forceBorder=True,
                fontSize=10,
            )
            y -= 18

    y -= 4

    # Troubleshooting flow: first-person narrative of how I worked through it
    if args.flow:
        y = ensure_space(c, y, 30, width, height, margin)
        y = draw_section_header(c, "How I Worked Through It", margin, y)
        y = draw_wrapped(c, args.flow, margin, y, text_width, body)
        y -= 4

    # Root cause
    y = ensure_space(c, y, 25, width, height, margin)
    y = draw_section_header(c, "Root Cause", margin, y)
    y = draw_wrapped(c, args.root_cause or EMPTY, margin, y, text_width, body)
    y -= 4

    # Resolution
    y = ensure_space(c, y, 25, width, height, margin)
    y = draw_section_header(c, "Resolution", margin, y)
    y = draw_wrapped(c, args.resolution or EMPTY, margin, y, text_width, body)
    y -= 4

    # Lesson learned
    y = ensure_space(c, y, 25, width, height, margin)
    y = draw_section_header(c, "Lesson Learned", margin, y)
    y = draw_wrapped(c, args.lesson or EMPTY, margin, y, text_width, body)
    y -= 8

    # End-of-case fillable summary
    y = ensure_space(c, y, 55, width, height, margin)
    y = draw_section_header(c, "Fill In Later", margin, y)

    c.setFont("Helvetica", 9)
    c.setFillColor(grey)
    c.drawString(margin, y - 4, "Follow-up actions:")
    c.setFillColor(black)
    c.acroForm.textfield(
        name="follow_up",
        tooltip="What still needs doing",
        x=margin,
        y=y - 36,
        width=text_width,
        height=28,
        borderColor=grey,
        fillColor=HexColor("#f5f7fa"),
        textColor=black,
        forceBorder=True,
        fontSize=10,
        fieldFlags="multiline",
    )
    y -= 42

    c.setFillColor(grey)
    c.drawString(margin, y - 4, "Time spent (mins):")
    c.setFillColor(black)
    c.acroForm.textfield(
        name="time_spent",
        tooltip="Minutes spent on this ticket",
        x=margin + 40 * mm,
        y=y - 9,
        width=30 * mm,
        height=12,
        borderColor=grey,
        fillColor=HexColor("#f5f7fa"),
        textColor=black,
        forceBorder=True,
        fontSize=10,
    )

    c.setFillColor(grey)
    c.drawString(margin + 85 * mm, y - 4, "Tags:")
    c.setFillColor(black)
    c.acroForm.textfield(
        name="tags",
        tooltip="Comma-separated tags for search",
        x=margin + 100 * mm,
        y=y - 9,
        width=70 * mm,
        height=12,
        borderColor=grey,
        fillColor=HexColor("#f5f7fa"),
        textColor=black,
        forceBorder=True,
        fontSize=10,
    )

    draw_footer(c, width)
    c.showPage()
    c.save()

    print(f"[OK] Saved: {filepath}")
    return filepath


if __name__ == "__main__":
    p = argparse.ArgumentParser()
    p.add_argument("--client", required=True)
    p.add_argument("--symptom", required=True)
    p.add_argument("--environment", default="")
    p.add_argument(
        "--step",
        action="append",
        default=[],
        help="One diagnostic step description. Pass --step multiple times for multiple steps.",
    )
    p.add_argument(
        "--step-how",
        action="append",
        default=[],
        dest="step_how",
        help=(
            "How-to guidance for the matching --step (pairs by order). "
            "Should be specific enough for a Level 1 engineer: exact cmdlet, "
            "exact menu path, what a good vs bad result looks like."
        ),
    )
    p.add_argument(
        "--question",
        action="append",
        default=[],
        help="A diagnostic question the wizard asked Visal during intake. Pairs with --answer by order.",
    )
    p.add_argument(
        "--answer",
        action="append",
        default=[],
        help="Visal's answer to the matching --question.",
    )
    p.add_argument(
        "--flow",
        default="",
        help=(
            "First-person narrative of how Visal worked through the case: "
            "what he tried first and why, how each result steered the next "
            "check, how he arrived at the root cause. 2 to 5 short paragraphs "
            "in his voice (no corporate fluff, no em/en dashes)."
        ),
    )
    p.add_argument("--root-cause", default="")
    p.add_argument("--resolution", default="")
    p.add_argument("--lesson", default="")
    p.add_argument("--engineer", default="Visal")
    p.add_argument("--out-dir", default="./case-log")
    args = p.parse_args()
    generate(args)
